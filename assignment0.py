#１ 変数の使い方
#変数を使って、「ねずこ」と「ぜんいつ」 は仲間ですとprint文を使って表示させてみよう
#なお、ねずこをname1、ぜんいつをname2として定義してください。

name1 = "ねずこ"
name2 = "ぜんいつ"
print(f"{name1}と{name2}は仲間です")

#２ if文の使い方
#１のソースを改造して、name2が敵キャラの「むざん」だった場合に
#仲間ではありませんと表示させてみよう。

name2 = "むざん"

if name2 == "むざん":
    print(f"{name2}仲間ではありません")
else:
    print(f"{name1}と{name2}は仲間です")

#３ 配列の使い方
#以下の配列に対して、キャラクター「ぜんいつ」を追加してみよう。 appendを使うことで追加できます。 name=["たんじろう","ぎゆう","ねずこ","むざん"]

name = ["たんじろう","ぎゆう","ねずこ","むざん"]

name.append("ぜんいつ")
print(name)

#４ for文の使い方
#３のソースコードで使用したnameのキャラクターをfor文を使って
#１行に１キャラクター表示してみよう

for i in name:
    print(i)

