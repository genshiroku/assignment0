#【仕様】
#関数名：なんでも良い
#引数：キャラクターの名前を格納する変数
#関数で行う処理：キャラクターのリスト（３、４で使ったもの）の中に、
#引数で入力されたキャラクター名が存在するか確認して結果をprint文で表示させる
#例）引数が「ぎゆう」→nmaeにぎゆうが存在する→ぎゆうは含まれますと表示    

def is_included(name):
    names = ["たんじろう","ぎゆう","ねずこ","むざん","ぜんいつ"]
    if name in names:
        print(f"{name}は含まれます")
    else:
        print(f"{name}はふくまれません")

is_included("ぎゆう")
is_included("こうへい")

